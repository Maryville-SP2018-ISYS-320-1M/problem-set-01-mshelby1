/* 7. Your pseudocode algorithm here
Create outer for Loop for how many lines I need
	Create inner for Loop for how long the lines are
		just use print for the symbol
	use print line for creating the lines
*/
public class P7_NestedLoopBox {

	public static void main(String[] args) {
		for (int i = 1; i <= 6; i++) {
			for (int j = 1; j <= 6; j++) {
				System.out.print("*");
			}
		System.out.println();
		}

	}

}

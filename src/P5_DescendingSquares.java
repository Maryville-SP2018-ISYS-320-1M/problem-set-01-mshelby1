/* 5. Your pseudocode algorithm here
Use for Loop to decrease squares of numbers from 10 to 1.
*/

public class P5_DescendingSquares {

	public static void main(String[] args) {
		for (int i = 10; i >= 1; i--) {
			System.out.print((i * i) + " ");
		}
	}

}
